// Criando um array vazio
let meuArray = [];

// Adicionando três números inteiros ao array usando o método push()
meuArray.push(5);
meuArray.push(10);
meuArray.push(15);

// Imprimindo os itens presentes no array
console.log("Itens no array:", meuArray);

// Substituindo o primeiro elemento (índice 0) pelo dobro do seu valor atual
meuArray[0] = meuArray[0] * 2;

// Imprimindo o array atualizado
console.log("Array atualizado:", meuArray);
